public class GreatestCommonDivisor {

    public static int getGreatestCommonDivisor(int a, int b) {
        if(a  < 10 || b < 10) {
            return -1;
        } else {
            int i =Math.max(a,b);
            while(true) {
                if ((a % i == 0) && (b % i == 0)) {
                    return i;
                }
                i--;
            }
       
    }

}
}
